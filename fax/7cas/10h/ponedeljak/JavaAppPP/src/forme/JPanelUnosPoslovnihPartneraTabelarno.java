    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        ModelTabelePoslovniPartnerUnos tm=(ModelTabelePoslovniPartnerUnos)jTablePoslovniPartneri.getModel();
        List<PoslovniPartner> partneri=tm.vratiListuPoslovnihPartnera();
        try{
            Kontroler.getInstance().sacuvajPoslovnePartnere(partneri);
            JOptionPane.showMessageDialog(this, "Uspesno sacuvani poslovni partneri!", "Cuvanje partnera", JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Poslovni partneri nisu sacuvani!\n"+e.getMessage(), "Cuvanje partnera", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSacuvajActionPerformed


