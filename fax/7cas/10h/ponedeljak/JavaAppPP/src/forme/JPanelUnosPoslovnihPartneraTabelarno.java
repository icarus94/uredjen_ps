private void pripremitabelu(){
    List<PoslovniPartner> partneri=new ArrayList<>();
    ModelTabelePoslovniPartnerUnos tm=new ModelTabelePoslovniPartnerUnos(partneri);
    jTablePoslovniPartneri.setModel(tm);
    
    List<Mesto> mesta=Kontroler.getInstance().vratiMesta();
    JComboBox jcbMesta=new JComboBox(mesta.toArray());
    TableColumnModel tcm=jTablePoslovniPartneri.getColumnModel();
    TableColumn tc=tcm.getColumn(5);
    tc.setCellEditor(new DefaultCellEditor(jcbMesta));
    
}



