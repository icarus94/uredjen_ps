    private void popuniKomboPredmeta() {
        ArrayList<Predmet> lista = Kontroler.getInstanca().dajListuIZKolekcije();
        
        cmbPredmet.removeAllItems();
        
        for (Predmet p : lista) {
            cmbPredmet.addItem(p);
        }
    }

    private void popuniKomboStudenata() {
        ArrayList<Student> lista = Kontroler.getInstanca().vratiStudente();
        cmbStudent.removeAllItems();
        for (Student s : lista) {
            cmbStudent.addItem(s);
        }
    }

    private void popuniOcene() {
        cmbOcena.removeAllItems();
        for (int i = 5; i <= 10; i++) {
            cmbOcena.addItem(i);
        }
    }
