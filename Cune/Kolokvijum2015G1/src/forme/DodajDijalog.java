    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // dodaj dugme
        
        Predmet p = (Predmet) cmbPredmet.getSelectedItem();
        Student s = (Student) cmbStudent.getSelectedItem();
        int ocena = (int) cmbOcena.getSelectedItem();
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        String dat = txtDatum.getText();
        
        Date datum;
        
        try {
            datum = sdf.parse(dat);
        } catch (ParseException ex) {
            System.err.println("Ne moze takav format");
            return;
        }
        Prijava pr = new Prijava(p, s, datum, ocena,"NEW");
        GlavnaForma gf = (GlavnaForma) getParent();
        
        gf.dodajPRijavu(pr);
    }//GEN-LAST:event_jButton1ActionPerformed

