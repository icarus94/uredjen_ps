    private void popuniPredmete() {
        jcmbPredmet.removeAllItems();
        
        ArrayList<Predmet> lista= Kontroler.getInstance().vratiListuPredmetaIzKolekcije();
        
        for (Predmet p : lista) {
            jcmbPredmet.addItem(p);
        }
    }

    private void popuniStudente() {

        jcmbStudent.removeAllItems();
        
        ArrayList<Student> lista= Kontroler.getInstance().vratiListuStudenta();
        
        for (Student s : lista) {
            jcmbStudent.addItem(s);
        }
    }

    private void popuniOcene() {
        jcmbOcena.removeAllItems();
        for (int i = 5; i <= 10; i++) {
            jcmbOcena.addItem(i);
        }
    }
