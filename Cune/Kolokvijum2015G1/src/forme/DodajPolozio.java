    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //dugme sacuvaj
       Predmet predmet = (Predmet) jcmbPredmet.getSelectedItem();
       Student student = (Student) jcmbStudent.getSelectedItem();
       int ocena= (int) jcmbOcena.getSelectedItem();
       
        Date datum;
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
        
        try {
            datum = sdf.parse(jtxtDatum.getText().trim());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Lose unesen datum");
            return;
        }
        
        Prijava prijava = new Prijava(predmet, student, datum, ocena, "Novi");
        
        Kontroler.getInstance().dodajPrijavu(prijava);
        gef.popuniTabelu();
    }//GEN-LAST:event_jButton1ActionPerformed

    

