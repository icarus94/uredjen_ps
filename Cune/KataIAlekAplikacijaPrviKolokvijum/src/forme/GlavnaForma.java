    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        
        Radnik r = (Radnik) cmbRadnik.getSelectedItem();
        Gradiliste g = (Gradiliste) cmbGradiliste.getSelectedItem();
        
        String brS = txtSati.getText();
        String dat = txtDatum.getText();
        if(brS.isEmpty() || dat.isEmpty()){
            JOptionPane.showMessageDialog(this, "Sva polja su ovacezna");
            return;
        }
        
        int brojSati = Integer.parseInt(brS);
        
       
        
        
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        Date datum = null;
        try {
            datum = sdf.parse(dat);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Nije dobar format. treba da bude dd.MM.yyyy");
            return;
        }
        
        Angazovanje a = new Angazovanje(-1, brojSati, datum, r, g);
        
        ModelTabeleAngazovanja mta = (ModelTabeleAngazovanja) tabelaAngozovanje.getModel();
        mta.dodaj(a);
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int red = tabelaAngozovanje.getSelectedRow();
        
        if(red == -1){
            JOptionPane.showMessageDialog(this, "Niste izabrali red", "Upozorenje", JOptionPane.WARNING_MESSAGE);
        }else{
            ModelTabeleAngazovanja mta = (ModelTabeleAngazovanja) tabelaAngozovanje.getModel();
            mta.obrisi(red);
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        
        ModelTabeleAngazovanja mta = (ModelTabeleAngazovanja) tabelaAngozovanje.getModel();
        ArrayList<Angazovanje> lista = mta.getLista();
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        
        for (int i = 0; i < lista.size(); i++) {
            String dat1 = sdf.format(lista.get(i).getDatum());
            int ukupnoSati = lista.get(i).getBrojSati();
            for (int j = i+1; j < lista.size(); j++) {
                String dat2 = sdf.format(lista.get(j).getDatum());
                
                if(lista.get(i).getRadnik().getRadnikID()== lista.get(j).getRadnik().getRadnikID() && dat1.equals(dat2)){
                    if(lista.get(i).getGradiliste().getGradilisteID()!=lista.get(j).getGradiliste().getGradilisteID()){
                        JOptionPane.showMessageDialog(this, "Gradiliste mora biti isto u istom danu", "greska", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    ukupnoSati+=lista.get(j).getBrojSati();
                }
            }
            
            if(ukupnoSati>8){
                JOptionPane.showMessageDialog(this, "Vise od 8 sati", "greska", JOptionPane.ERROR_MESSAGE);
                        return;
            }
        }
        
        boolean sacuvano = Kontroler.getInstanca().sacuvajSvaAngazovanja(lista);
        
        
        if(sacuvano){
            JOptionPane.showMessageDialog(this, "Sve uspesno sacuvano", "uspeh", JOptionPane.INFORMATION_MESSAGE);       
        }else{
            JOptionPane.showMessageDialog(this, "Sjebao si nesto", "greska", JOptionPane.ERROR_MESSAGE);       
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

