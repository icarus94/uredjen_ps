    private void srediKomboRadnika() {
        
        cmbRadnik.removeAllItems();
        
        ArrayList<Radnik> listaRadnika = Kontroler.getInstanca().vratiListuRadnika();
        
        for (Radnik radnik : listaRadnika) {
            cmbRadnik.addItem(radnik);
        }
    }

    private void srediKomboGradilista() {
        cmbGradiliste.removeAllItems();
        
        ArrayList<Gradiliste> lista = Kontroler.getInstanca().vratiGradilista();
        
        for (Gradiliste gradiliste : lista) {
            cmbGradiliste.addItem(gradiliste);
        }

    }

