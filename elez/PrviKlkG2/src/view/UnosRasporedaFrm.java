    private void popuniRanike() {
        jComboBoxRadnik.removeAllItems();
        List<Radnik> radnici = Controller.getInstance().vratiRadnike();
        for(Radnik r: radnici){
            jComboBoxRadnik.addItem(r);
        }
        
    }

    private void popuniLokacije() {
        jComboBoxLokacija.removeAllItems();
        List<Lokacija> lokacije = Controller.getInstance().vratiLokacije();
        for(Lokacija l: lokacije){
            jComboBoxLokacija.addItem(l);
        }
    }
    public void srediTabelu(){
        RasporedTableModel rtm = new RasporedTableModel();
        jTableRaspored.setModel(rtm);
    }
    public Raspored kreirajRaspored(String s, String d, Radnik r, Lokacija l) throws ValidacijaException{
        if(s.isEmpty() || d.isEmpty()){
            throw new ValidacijaException("Morate uneti sve vrednosti");
        }
        int sati;
        Date datum;
        try{
            sati = Integer.parseInt(s);
        }catch (Exception ex){
            throw new ValidacijaException("Morate uneti cele brojeve za broj sati");
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        java.util.Date dat;
        try{
            dat = sdf.parse(d);
            datum = new Date(dat.getTime());
        } catch (ParseException ex) {
            throw new ValidacijaException("Morate uneti datum u formatu dd.MM.yyyy");
        }
        
        return new Raspored(-1, sati, datum, r, l);
    }

    private void ocistiPolja() {
        jComboBoxLokacija.setSelectedIndex(0);
        jComboBoxRadnik.setSelectedIndex(0);
        jTextFieldDatum.setText("");
        jTextFieldSati.setText("");
    }
