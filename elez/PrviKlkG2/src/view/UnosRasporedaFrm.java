    private void jButtonDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajActionPerformed
        // TODO add your handling code here:
        Lokacija l = (Lokacija)jComboBoxLokacija.getSelectedItem();
        Radnik r = (Radnik) jComboBoxRadnik.getSelectedItem();
        String sati = jTextFieldSati.getText();
        String datum = jTextFieldDatum.getText();
        try {
            Raspored raspored = kreirajRaspored(sati, datum, r, l);
            RasporedTableModel rtm = (RasporedTableModel) jTableRaspored.getModel();
            rtm.dodajRaspored(raspored);
            ocistiPolja();
        } catch (ValidacijaException ex) {
            JOptionPane.showMessageDialog(this,ex.getMessage(),"Greska!",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDodajActionPerformed

